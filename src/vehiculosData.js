// Obtener lista de marcas ordenadas alfabéticamente
export const getMarcas = () => {
  return Object.keys(marcasYModelos).sort();
};

// Obtener modelos según la marca
export const getModelos = (marca) => {
  return marcasYModelos[marca] || [];
};

// Años disponibles (últimos 30 años + próximo año)
export const getAnios = () => {
  const anioActual = new Date().getFullYear();
  const anios = [];
  for (let i = anioActual + 1; i >= anioActual - 29; i--) {
    anios.push(i);
  }
  return anios;
};

// Obtener nombre legible de marca (reemplaza guiones bajos por espacios)
export const getNombreMarcaLegible = (marca) => {
  return marca.replace(/_/g, ' ');
};